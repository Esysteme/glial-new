<h4>Qu'est-ce qu'une cellule Glial ?</h4>


<p>Dans le système nerveux, les cellules gliales (parfois nevroglie ou tout simplement glie, du grec γλοιός (gloios), « gluant ») sont les cellules qui forment l'environnement des neurones. Elles assurent le maintien de l'homéostasie, produisent la myéline et jouent un rôle de soutien et de protection du tissu nerveux en apportant les nutriments et l'oxygène, en éliminant les cellules mortes et en combattant les pathogènes.</p>

<p>Les cellules gliales représentent environ 50 % du volume cérébral1 et au plus 50 % des cellules du cerveau2, contrairement à l'assertion très répandue affirmant des ratios de 10:1 à 50:13, sans aucune référence sérieuse. On distingue en général 4 principaux types de cellules gliales :</p>
<ul>
    <li>les astrocytes ;</li>
    <li>les oligodendrocytes ;</li>
    <li>les cellules de Schwann ;</li>
    <li>la microglie.</li>
</ul>
<p>Contrairement à la grande majorité des neurones, les cellules gliales peuvent se diviser par mitose.</p>

<p>Pendant longtemps, l'implication des cellules gliales dans le traitement de l'information nerveuse a été sous estimée par rapport au rôle proéminent des neurones, mais il est aujourd'hui reconnu qu'elles exercent une action modulatrice sur la neurotransmission bien que le détail de ces mécanismes reste mal compris. </p>