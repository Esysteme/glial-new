<h4>Qu'est-ce qu'une cellule Glial ?</h4>

<p>Glial cells also called neuroglia, or simply glia (Greek γλία and γλοία "glue"; pronounced in English as either /ˈɡliːə/ or /ˈɡlaɪə/), are non-neuronal cells that maintain homeostasis, form myelin, and provide support and protection for neurons in the central and peripheral nervous systems.[1] In the central nervous system, glial cells include oligodendrocytes, astrocytes, ependymal cells and microglia, and in the peripheral nervous system glial cells include Schwann cells and satellite cells.</p>

<p>As the Greek name implies, glia are commonly known as the glue of the nervous system; however, this is not fully accurate. Glia were discovered in 1856, by the pathologist Rudolf Virchow in his search for a "connective tissue" in the brain.[2] Neuroscience currently identifies four main functions of glial cells:</p>
<ol>
    <li>To surround neurons and hold them in place</li>
    <li>To supply nutrients and oxygen to neurons</li>
    <li>To insulate one neuron from another</li>
    <li>To destroy pathogens and remove dead neurons.</li>
</ol>
<p>For over a century, it was believed that the neuroglia did not play any role in neurotransmission. However 21st century neuroscience has recognized that glial cells do have some effects on certain physiological processes like breathing,[3][4] and in assisting the neurons to form synaptic connections between each other.[5]</p>

